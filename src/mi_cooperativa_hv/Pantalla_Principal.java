    private void AGREGAR_AFILIADO_BTTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AGREGAR_AFILIADO_BTTActionPerformed
        String primerNombre = PrimerNombre.getText().trim();
        String segundoNombre = this.SegundoNombre.getText().trim();
        String primerApellido = PrimerApellido.getText().trim();
        String segundoApellido = SegundoApellido.getText().trim();
        String calle = Calle.getText().trim();
        String avenida = Avenida.getText().trim();
        String numCasa = NumCasa.getText().trim();
        String ciudad = Ciudad.getText().trim();
        String departamento = Depto.getText().trim();
        String referencia = Reff.getText().trim();
        String correoPrimario = Correo1.getText().trim();
        String correoSecundario = Correo2.getText().trim();
        Date fechaNacimiento = FechaNacimiento_cb.getDate();

        if (primerNombre.isEmpty() || primerApellido.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El primer nombre y el primer apellido son obligatorios", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (telefonos.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe agregar al menos un tel√©fono", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (calle.isEmpty() || ciudad.isEmpty() || departamento.isEmpty() || correoPrimario.isEmpty() || fechaNacimiento == null) {
            JOptionPane.showMessageDialog(this, "Todos los campos obligatorios deben estar completos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (Database.agregarAfiliado(primerNombre, segundoNombre, primerApellido, segundoApellido, calle, avenida, numCasa, ciudad, departamento, referencia, correoPrimario, correoSecundario, fechaNacimiento, telefonos, User_Actual)) {
            JOptionPane.showMessageDialog(this, "Afiliado y tel√©fonos agregados correctamente", "√âxito", JOptionPane.INFORMATION_MESSAGE);
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo registrar el usuario", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_AGREGAR_AFILIADO_BTTActionPerformed

